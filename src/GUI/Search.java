    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        String genre = (String) jComboBox1.getSelectedItem();
        for (int i=0; i<Tabs.moviesArray.size(); i++){
            Movies movies = Tabs.moviesArray.get(i);
            if(movies.Genre.equalsIgnoreCase(genre)){
                model.addRow(new Object[]{movies.Name, "Actors", movies.Genre,
                    movies.PlayTime, "Image"});
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        String actor = jTextField1.getText();
        for (int i=0; i<Tabs.moviesArray.size(); i++){
            Movies movies = Tabs.moviesArray.get(i);
            if (movies.Actors.contains(actor)){
                model.addRow(new Object[]{movies.Name, "Actors", movies.Genre,
                    movies.PlayTime, "Image"});
                } 
        }
    }//GEN-LAST:event_jButton2ActionPerformed

