    private void createAutocompete(){
        jTextField1.setFocusTraversalKeysEnabled(false);
        
        for (int i=0; i<Tabs.moviesArray.size(); i++){
            Movies movies = Tabs.moviesArray.get(i);
            for (int j=0; j<movies.Actors.size(); j++){  
                if(!keywords.contains(movies.Actors.get(j))){
                    keywords.add(movies.Actors.get(j));
                }
            }
        }
        
        Autocomplete autoComplete = new Autocomplete(jTextField1, keywords);
        jTextField1.getDocument().addDocumentListener(autoComplete);
        
        jTextField1.getInputMap().put(KeyStroke.getKeyStroke("ENTER"), COMMIT_ACTION);
        jTextField1.getActionMap().put(COMMIT_ACTION, autoComplete.new CommitAction());
    }

